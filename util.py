def reconstructionError(projections,X):
    W = projections.components_
    if sps.issparse(W):
        W = W.todense()
    p = pinv(W)
    reconstructed = np.matmul(np.matmul(p,W),X.T).T # Unproject projected data
    errors = np.square(X-reconstructed)
    return np.nanmean(errors)